class Teacher {
  final String name;
  final String subject;
  final String level;
  final String price;
  final String email;
  final String phone;
  final String city;
  final double rating;
  final String avatarUrl;

  Teacher({
    required this.name,
    required this.subject,
    required this.level,
    required this.price,
    required this.email,
    required this.phone,
    required this.city,
    required this.rating,
    required this.avatarUrl,
  });
} 